    private void ButStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButStartActionPerformed
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {

                int maxtLength = 1000;
                byte[] buf = new byte[maxtLength];
                DatagramPacket dgp = new DatagramPacket(buf, maxtLength);
                try {
                    DatagramSocket dgs = new DatagramSocket(8080);
                    //tell this socket to listen for data
                    dgs.receive(dgp);//Blocking coz kÃªp listening for data
                    //Get back the data out of the packer
                    
                    String line = new String(dgp.getData(),0, dgp.getLength());
                    lblMessage.setText(line);
                } catch (SocketException ex) {
                    Logger.getLogger(SesUDPReceiver.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(SesUDPReceiver.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
        t.start();
    }//GEN-LAST:event_ButStartActionPerformed

